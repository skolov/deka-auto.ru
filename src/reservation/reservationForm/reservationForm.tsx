export default class ReservationForm extends React.Component<any, any>  {

  
  
  state = {
    senderStatus: false,
    showWarning: false,
    showNextStep: false,
    data:{
      name: '',
      email: '',
      phone: '',
      description: '',
      mark: 'AC',
      model: 'A1'
    },
    errors: {
      name: true,
      email: true,
      phone: true,
      description: true,
      mark: true,
      model: true
    },
    errorsTexts:{
      name: "Имя введено некорректно!",
      email: "E-mail введен некорректно!",
      phone: "Номер телефона введен некорректно!",
      description: "Описание введено некорректно!",
      mark: "Выберите марку своего автомобиля!",
      model: "Выберите модель своего автомобиля!"
    }
  }

  
  
  handleInputChange = (event: any): void => {
    const target = event.target;
    const value = target.type === 'checkbox' ? target.checked : target.value;
    const type = target.name;    
    
    this.setState({
      data: Object.assign({}, this.state.data,{
        [type]: value
      })
    })

    this.checkValid({type, value})
  }

  
  
  handlerInputKeyDown = (): void => {
    this.setState({showWarning: false})
  }



  checkValid = ({ type, value }: { type: string; value: string; }): void => {
    let regExp: any = {
      'name': /^[a-zA-Zа-яА-ЯЁ0-9\s]{3,100}$/, // eslint-disable-next-line
      'email': /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/, // eslint-disable-next-line
      'phone': /^((8|\+7)[\- ]?)?(\(?\d{3}\)?[\- ]?)?[\d\- ]{7,17}$/, // eslint-disable-next-line
      'description': /^[a-zA-Zа-яА-ЯЁё0-9!\-'/:();.,*_+=\s]{5,2000}$/ // eslint-disable-next-line
    }

    this.setState({
      errors: Object.assign({}, this.state.errors, {
        [type]: value.match(regExp[type]) ? true : false
      })
    })
  }



  checkForEmptiness = (): boolean => {
    let arr: Array<string> = Object.values(this.state.data),
        decision: boolean = arr.includes('') ? true : false;
    this.setState({showWarning: decision});
    return decision;
  }



  checkForErrors = (): boolean=> {
    let arr: Array<boolean> = Object.values(this.state.errors),
        decision: boolean = arr.includes(false) ? true : false;
    this.setState({showWarning: decision});
    return decision;
  }



  showNextStep = ():void => {
    let arr: Array<boolean> = Object.values(this.state.errors);

    if(
        this.state.data.name && 
        this.state.data.email && 
        this.state.data.phone &&
        !arr.includes(false)
    ) {
      this.setState({showNextStep: true})
    } else {
      this.setState({showWarning: true})
    }
  }



  sendData = async () => {
    let url = '/scripts/newclient.php';

    if(!this.checkForEmptiness() && !this.checkForErrors()){

      let response = await fetch(url, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json;charset=utf-8'
        },
        body: JSON.stringify(this.state.data)
      });

      if (response.ok) {
        this.setState({senderStatus: true})
      } else {
        // Обработка 
      }
     
    }    
  }



  public render() {

    return (
      <form>
        <div className={styleReservation.resInside}>
          <div className={styleReservation.header}>
            <h2>Запись на ремонт</h2>
          </div>

          {!this.state.showNextStep &&
            <div className={styleReservation.body}>
              <div className={styleReservation.text}>
                С помощью данной формы Вы можете произвести запись в автосервис "ДЕКА".
              </div>

              <div className={styleReservation.input}>
                <InputText 
                  placeholder="Как к Вам обращаться?"
                  name="name"
                  updateStateProp={this.handleInputChange}
                  getKeyDown={this.handlerInputKeyDown}
                  data={{
                    "value": this.state.data.name,
                    "error": this.state.errors.name,
                    "errorText": this.state.errorsTexts.name,
                  }} 
                />
              </div>

              <div className={styleReservation.input}>
                <InputText 
                  placeholder="Введите Ваш E-mail"
                  name="email"
                  updateStateProp={this.handleInputChange} 
                  getKeyDown={this.handlerInputKeyDown}
                  data={{
                    "value": this.state.data.email,
                    "error": this.state.errors.email,
                    "errorText": this.state.errorsTexts.email,
                  }}  
                />
              </div>

              <div className={styleReservation.input}>
                <InputText 
                  placeholder="Введите Ваш номер телефона"
                  name="phone"
                  updateStateProp={this.handleInputChange} 
                  getKeyDown={this.handlerInputKeyDown}
                  data={{
                    "value": this.state.data.phone,
                    "error": this.state.errors.phone,
                    "errorText": this.state.errorsTexts.phone,
                  }}  
                />
              </div>
            </div>
          }

          {!this.state.showNextStep &&
            <div className={styleReservation.footer}>
              <div className={styleReservation.button} onClick={this.showNextStep}>Далее</div>
              {this.state.showWarning && 
                <div style={{color: "red", fontSize: "12px", margin: "10px 20px"}}>Необходимо корректно заполнить все поля!</div>
              }
            </div>
          }


          {this.state.showNextStep &&
            <div className={styleReservation.body}>

              <div className={styleReservation.selectHolder}>
                <div className={styleReservation.selectLabel}>
                  Выберите марку Вашего автомобиля:
                </div>
                <Select 
                  options={DATA} 
                  name="mark"
                  updateStateProp={this.handleInputChange} 
                  data={{
                    "value": this.state.data.mark,
                    "error": this.state.errors.mark,
                    "errorText": this.state.errorsTexts.mark,
                  }} 
                />
              </div>

              <div className={styleReservation.selectHolder}>
                <div className={styleReservation.selectLabel}>
                  Выберите марку Вашего автомобиля:
                </div>
                <Select 
                  options={postData[this.state.data.mark]} 
                  name="model"
                  updateStateProp={this.handleInputChange} 
                  data={{
                    "value": this.state.data.model,
                    "error": this.state.errors.model,
                    "errorText": this.state.errorsTexts.model,
                  }} 
                />
              </div>

              <div className={styleReservation.input}>
                <Textarea 
                  placeholder="Введите краткое описание проблемы"
                  name="description"
                  updateStateProp={this.handleInputChange} 
                  getKeyDown={this.handlerInputKeyDown}
                  data={{
                    "value": this.state.data.description,
                    "error": this.state.errors.description,
                    "errorText": this.state.errorsTexts.description,
                  }}  
                />
              </div>
            </div>
          }

          {this.state.showNextStep &&
            <div className={styleReservation.footer}>
              <div className={styleReservation.button} onClick={this.sendData}>Записаться</div>
              {this.state.showWarning && 
                <div style={{color: "red", fontSize: "12px", margin: "10px 20px"}}>Необходимо корректно заполнить все поля!</div>
              }
            </div>
          }
          

          {this.state.senderStatus && 
            <div className={styleReservation.registered}>
              <div>
                <p>Запись произведена успешно, в ближайшее время мы с Вами свяжемся</p>
                <span>
                  <svg xmlns="http://www.w3.org/2000/svg" width="50" height="50" viewBox="0 0 24 24" fill="green"><path d="M20.285 2l-11.285 11.567-5.286-5.011-3.714 3.716 9 8.728 15-15.285z"/></svg>
                </span>
              </div>
            </div>
          }
        </div>
      </form>
    );
  }
}
  